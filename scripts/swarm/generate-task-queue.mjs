#!/usr/bin/env node
// scripts/swarm/generate-task-queue.mjs
//
// SwarmOps 閉ループ 4 段目:
//   criteria-violations.json (TQ-118) + analytics-export.json (TQ-120)
//   → docs/swarmops/task-proposals.md に改善候補を score 順で書く
//
// TASK_QUEUE.md は**自動で書き換えない**（owner 採否待ちの中間成果物）。
//
// 使い方:
//   node scripts/swarm/generate-task-queue.mjs            # 既定 top 5, ファイル書き込み
//   node scripts/swarm/generate-task-queue.mjs --dry-run  # stdout のみ
//   node scripts/swarm/generate-task-queue.mjs --top 3
//   node scripts/swarm/generate-task-queue.mjs --max-age-days 7
//   node scripts/swarm/generate-task-queue.mjs \
//     --violations path/to/violations.json \
//     --analytics path/to/analytics.json \
//     --manifest path/to/manifest.yaml \
//     --out path/to/task-proposals.md
//
// 入力フォーマット:
//   TQ-118: { nodes: { <id>: ["steps_exceeded", ...] }, scores: { <id>: 3.5 }, weights, generatedAt }
//   TQ-120: { exportedAt, events: [{ event, timestamp, properties: { ... } }] }
//   Sentry: { generatedAt, buckets: [{ requestId, count, sample_path }] } (optional)
//
// 出力:
//   docs/swarmops/task-proposals.md
//     先頭 `# Task Proposals` ヘッダーと preamble を保持したまま、
//     `<!-- auto-generated:start -->` と `<!-- auto-generated:end -->` の
//     間のみ書き換える（diff が見やすいように）。

