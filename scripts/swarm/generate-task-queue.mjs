/**
 * Violations proposal builder.
 * Shape: { id, source, score, priority, rationale, ac, touchingFiles, nodeId }
 */
export function buildViolationProposals(violations, nodesById, { maxAgeDays, now }) {
  if (!violations || typeof violations !== 'object') return []
  const generatedAt = violations.generatedAt
  if (generatedAt) {
    const ageMs = new Date(now).getTime() - new Date(generatedAt).getTime()
    if (!Number.isFinite(ageMs)) {
      // ignore unparseable timestamp, treat as fresh
    } else if (ageMs > maxAgeDays * 24 * 60 * 60 * 1000) {
      return []
    }
  }
  const scores = violations.scores || {}
  const nodes = violations.nodes || {}
  const out = []
  for (const [nodeId, score] of Object.entries(scores)) {
    const tags = Array.isArray(nodes[nodeId]) ? nodes[nodeId] : []
    const node = nodesById.get(nodeId) ?? { id: nodeId }
    const priority = derivePriority(Number(score) || 0, Boolean(node.critical_path === true || node.critical_path === 'true'))
    out.push({
      id: nodeId,
      source: 'criteria-violations',
      score: Number(score) || 0,
      priority,
      rationale: formatViolationRationale(nodeId, tags, node),
      ac: formatViolationAc(nodeId, tags),
      touchingFiles: predictFilesForNode(node),
      nodeId,
    })
  }
  return out
}

const KNOWN_EVENTS = new Set([
  'lesson_started',
  'blocked',
  'evidence_passed',
  'plan_revised',
  'hearing_started',
  'hearing_completed',
  'plan_compiled',
])

/**
 * Analytics proposal builder.
 * Heuristic:
 *  - `blocked` イベントが発生している (target_testid, path) をバケット集計し、件数多いもの P1
 *  - `lesson_started` のドメイン分布から、ゼロ件ドメインを P2 で提案
 *  - `evidence_passed` が無いのに `lesson_started` がある track は P1 で warn
 *  - `plan_revised` の reason 上位を P2 で提案
 */
export function buildAnalyticsProposals(analytics) {
  if (!analytics || typeof analytics !== 'object') return []
  const events = Array.isArray(analytics.events) ? analytics.events : []
  const proposals = []

  const blockedBuckets = new Map() // key -> { count, sample }
  const lessonStartedByDomain = new Map() // domain -> count
  const evidencePassedByDomain = new Map()
  const planRevisedReasons = new Map()

  for (const ev of events) {
    if (!ev || typeof ev !== 'object') continue
    const name = typeof ev.event === 'string' ? ev.event : ''
    const props = ev.properties && typeof ev.properties === 'object' ? ev.properties : {}
    if (!KNOWN_EVENTS.has(name)) continue
    if (name === 'blocked') {
      const key = `${props.target_testid ?? '?'}@${props.path ?? '?'}`
      const bucket = blockedBuckets.get(key) ?? { count: 0, sample: props }
      bucket.count += 1
      blockedBuckets.set(key, bucket)
    } else if (name === 'lesson_started') {
      const d = typeof props.domain === 'string' ? props.domain : 'unknown'
      lessonStartedByDomain.set(d, (lessonStartedByDomain.get(d) ?? 0) + 1)
    } else if (name === 'evidence_passed') {
      // domain はイベントに直接ないことが多いので milestone_id prefix で推測
      const d = typeof props.domain === 'string' ? props.domain : 'unknown'
      evidencePassedByDomain.set(d, (evidencePassedByDomain.get(d) ?? 0) + 1)
    } else if (name === 'plan_revised') {
      const r = typeof props.reason === 'string' ? props.reason : 'unspecified'
      planRevisedReasons.set(r, (planRevisedReasons.get(r) ?? 0) + 1)
    }
  }

  // 1) blocked buckets (usually highest signal)
  for (const [key, bucket] of blockedBuckets.entries()) {
    const score = 1.5 + Math.log2(bucket.count + 1) // 1 event → 1.5, 8 events → ~4.5
    proposals.push({
      id: `analytics/blocked:${key}`,
      source: 'analytics',
      score,
      priority: derivePriority(score, false),
      rationale: `PostHog \`blocked\` event が ${bucket.count} 件 (testid=${bucket.sample.target_testid ?? '?'}, path=${bucket.sample.path ?? '?'}, reason=${bucket.sample.reason ?? '?'})。ユーザーが disabled/aria-disabled で詰まっている。`,
      ac: `\`${bucket.sample.path ?? '?'}\` の \`data-testid="${bucket.sample.target_testid ?? '?'}"\` が blocked にならない E2E を追加し、blocked event が 7 日で 0 件になること。`,
      touchingFiles: [
        'apps/web/src/lib/analytics/hooks/useBlockedTracker.ts',
        `apps/web/e2e/**/${slug(bucket.sample.target_testid ?? 'blocked')}.spec.ts`,
      ],
    })
  }

  // 2) lesson_started が 0 のドメイン（= トラック露出不足）
  const knownDomains = ['ai-content-creator', 'ai-app-builder', 'ai-business-automation']
  for (const d of knownDomains) {
    const count = lessonStartedByDomain.get(d) ?? 0
    if (count === 0) {
      const score = 2.0
      proposals.push({
        id: `analytics/no-lesson-started:${d}`,
        source: 'analytics',
        score,
        priority: derivePriority(score, false),
        rationale: `PostHog export 期間中 \`lesson_started\` が \`domain=${d}\` で 0 件。トラックが導線から発見されていない可能性。`,
        ac: `\`/lessons?domain=${d}\` を訪れた学習者のうち ≥5% が 7 日以内に \`lesson_started\` を発火すること。E2E: recommend panel に当該トラックの atom が 1 件以上並ぶ。`,
        touchingFiles: [
          'apps/web/src/app/(learner)/lessons/page.tsx',
          'apps/web/src/components/recommendations/*',
        ],
      })
    }
  }

  // 3) lesson_started あり + evidence_passed 皆無
  const lessonAnyDomain = [...lessonStartedByDomain.entries()].find(([, c]) => c > 0)
  if (lessonAnyDomain && evidencePassedByDomain.size === 0) {
    const score = 3.0
    proposals.push({
      id: 'analytics/no-evidence-passed',
      source: 'analytics',
      score,
      priority: derivePriority(score, false),
      rationale: `\`lesson_started\` (${lessonAnyDomain[1]} 件) は発生しているが \`evidence_passed\` が 0 件。artifact 検証まで到達できていない。`,
      ac: 'レッスン完了 → artifact submit → evidence_passed まで一気通貫する E2E を 1 本通し、PostHog の evidence_passed が 7 日で ≥1 件計測されること。',
      touchingFiles: [
        'apps/web/src/app/api/artifacts/verify/route.ts',
        'apps/web/src/components/artifact/ArtifactSubmitPanel.tsx',
      ],
    })
  }

  // 4) plan_revised の上位 reason
  const topReason = [...planRevisedReasons.entries()].sort((a, b) => b[1] - a[1])[0]
  if (topReason && topReason[1] >= 3) {
    const score = 1.5 + Math.log2(topReason[1])
    proposals.push({
      id: `analytics/plan-revision:${slug(topReason[0])}`,
      source: 'analytics',
      score,
      priority: derivePriority(score, false),
      rationale: `\`plan_revised\` の reason 上位が "${topReason[0]}" (${topReason[1]} 件)。プラン初期品質が不足。`,
      ac: `初回 plan_compiled 時点で reason="${topReason[0]}" が発動しない hearing 質問テンプレを追加し、plan_revised の rate を 50% 以下に下げる。`,
      touchingFiles: [
        'apps/web/src/lib/hearing/*',
        'apps/web/src/lib/plan/compile.ts',
      ],
    })
  }

  return proposals
}

export function buildSentryProposals(sentry) {
  if (!sentry || typeof sentry !== 'object') return []
  const buckets = Array.isArray(sentry.buckets) ? sentry.buckets : []
  const out = []
  for (const b of buckets) {
    if (!b || typeof b !== 'object') continue
    const count = Number(b.count) || 0
    if (count < 3) continue
    const score = 1.5 + Math.log2(count)
    out.push({
      id: `sentry/${b.requestId ?? 'unknown'}`,
      source: 'sentry',
      score,
      priority: derivePriority(score, false),
      rationale: `Sentry に同一 requestId (${b.requestId ?? '?'}) が ${count} 件発生。path=${b.sample_path ?? '?'}.`,
      ac: `${b.sample_path ?? '?'} のエラーを再現する E2E を追加し Sentry event が 7 日で 0 件になること。`,
      touchingFiles: [b.sample_path ? routeFromPath(b.sample_path) : 'apps/web/src/app/**/route.ts'],
    })
  }
  return out
}

// ---------------------------------------------------------------------------
// Formatting
// ---------------------------------------------------------------------------

function derivePriority(score, isCriticalPath) {
  let base
  if (score >= 4.0) base = 0
  else if (score >= 2.0) base = 1
  else base = 2
  if (isCriticalPath && base > 0) base -= 1
  return `P${base}`
}

function formatViolationRationale(nodeId, tags, node) {
  const cp = node.critical_path === true || node.critical_path === 'true' ? ' (critical_path)' : ''
  const persona = node.persona ? ` persona=${node.persona}` : ''
  return `node \`${nodeId}\`${cp}${persona} が \`${tags.join(', ')}\` で warn を検知。describe="${node.describe ?? '?'}"`
}

function formatViolationAc(nodeId, tags) {
  const tagHints = tags
    .map((t) => {
      switch (t) {
        case 'steps_exceeded':
          return 'ステップ数を criteria 以下に収める'
        case 'ai_friction_exceeded':
          return 'AI 応答の friction 指標を閾値未満に下げる'
        case 'duration_exceeded':
          return 'ジャーニー所要時間を criteria 以下に短縮'
        case 'code_input_present':
          return '学習者の手動コード入力を排除（UI 誘導 / mentor fallback）'
        case 'blocked_transitions':
          return '導線上の disabled/aria-disabled を解消'
        default:
          return `${t} を解消`
      }
    })
    .join(' / ')
  return `\`${nodeId}\` spec を再実行し \`criteriaViolations\` が空 (${tagHints}) になること。`
}

function predictFilesForNode(node) {
  const files = new Set()
  if (node.spec_file) files.add(`apps/web/e2e/${node.spec_file}`)
  if (node.persona) files.add('apps/web/e2e/personas/*.spec.ts')
  // heuristic: AUTH-* → auth routes, AC-* → planner
  if (/^AUTH-/.test(node.id)) files.add('apps/web/src/app/(auth)/**/page.tsx')
  if (/^AC-/.test(node.id)) files.add('apps/web/src/app/(learner)/plan/**/*.tsx')
  if (/^LESSON-/.test(node.id)) files.add('apps/web/src/app/(learner)/lessons/**/*.tsx')
  if (files.size === 0) files.add('apps/web/src/**')
  return [...files]
}

function routeFromPath(p) {
  if (!p) return 'apps/web/src/app/**/route.ts'
  // /api/foo/bar → apps/web/src/app/api/foo/bar/route.ts
  if (p.startsWith('/api/')) return `apps/web/src/app${p}/route.ts`
  return `apps/web/src/app${p}/page.tsx`
}

function slug(s) {
  return String(s)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 48) || 'x'
}

function priorityRank(p) {
  // P0 → 0, P1 → 1, P2 → 2; lower rank = higher priority
  const n = Number(String(p).replace(/^P/i, ''))
  return Number.isFinite(n) ? n : 99
}

export function rankProposals(all, top) {
  return [...all]
    .sort((a, b) => {
      const pa = priorityRank(a.priority)
      const pb = priorityRank(b.priority)
      if (pa !== pb) return pa - pb
      if (a.score !== b.score) return b.score - a.score
      return String(a.id).localeCompare(String(b.id))
    })
    .slice(0, top)
}

export function renderMarkdown(proposals, { now }) {
  const lines = []
  lines.push(AUTO_BEGIN)
  lines.push('')
  lines.push(`_Generated at ${now} — top ${proposals.length} candidate(s), sorted by priority then score._`)
  lines.push('')
  if (proposals.length === 0) {
    lines.push('_現在、提案すべき候補はありません。verify が GREEN で analytics も静かです。_')
    lines.push('')
    lines.push(AUTO_END)
    return lines.join('\n') + '\n'
  }
  for (let i = 0; i < proposals.length; i++) {
    const p = proposals[i]
    lines.push(`## ${i + 1}. ${p.priority} — ${p.id} (score ${p.score.toFixed(2)})`)
    lines.push('')
    lines.push(`- **根拠**: ${p.rationale}`)
    lines.push(`- **完了条件案**: ${p.ac}`)
    lines.push(`- **source**: \`${p.source}\``)
    lines.push(`- **touching files 予測**:`)
    for (const f of p.touchingFiles) lines.push(`  - \`${f}\``)
    lines.push('')
  }
  lines.push(AUTO_END)
  return lines.join('\n') + '\n'
}

// ---------------------------------------------------------------------------
// File write with header preservation
// ---------------------------------------------------------------------------

export function mergeIntoExisting(existing, newBody) {
  // if file has auto-generated block, replace it; else append with default header
  if (existing && existing.includes(AUTO_BEGIN) && existing.includes(AUTO_END)) {
    const before = existing.slice(0, existing.indexOf(AUTO_BEGIN))
    const after = existing.slice(existing.indexOf(AUTO_END) + AUTO_END.length)
    // normalise: ensure newline separators around block
    const head = before.endsWith('\n') ? before : `${before}\n`
    const tail = after.startsWith('\n') ? after : `\n${after}`
    return `${head}${newBody.trimEnd()}${tail}`
  }
  // fresh or non-managed file: replace entirely with default header + body
  const headerSource = existing && existing.trim().length > 0 ? existing.trimEnd() + '\n\n' : DEFAULT_HEADER + '\n'
  return `${headerSource}${newBody}`
}

// ---------------------------------------------------------------------------
// Entry point
// ---------------------------------------------------------------------------

export async function run(args) {
  const now = args.now ?? new Date().toISOString()

  const violations = readJsonOrNull(args.violations)
  const analytics = readJsonOrNull(args.analytics)
  const sentry = existsSync(args.sentry) ? readJsonOrNull(args.sentry) : null

  const manifestText = readTextOrNull(args.manifest)
  const nodes = manifestText ? parseManifestNodes(manifestText) : []
  const nodesById = new Map(nodes.map((n) => [n.id, n]))

  const all = [
    ...buildViolationProposals(violations, nodesById, { maxAgeDays: args.maxAgeDays, now }),
    ...buildAnalyticsProposals(analytics),
    ...buildSentryProposals(sentry),
  ]
  const ranked = rankProposals(all, args.top)
  const body = renderMarkdown(ranked, { now })

  if (args.dryRun) {
    process.stdout.write(body)
    return { proposals: ranked, wrote: false, path: args.out }
  }

  mkdirSync(dirname(args.out), { recursive: true })
  const existing = readTextOrNull(args.out) ?? ''
  const merged = mergeIntoExisting(existing, body)
  writeFileSync(args.out, merged)
  process.stdout.write(`==> generate-task-queue: wrote ${ranked.length} proposal(s) to ${args.out}\n`)
  return { proposals: ranked, wrote: true, path: args.out }
}

// ---------------------------------------------------------------------------
// main
// ---------------------------------------------------------------------------

const isMain = import.meta.url === `file://${process.argv[1]}`
if (isMain) {
  try {
    const args = parseArgs(process.argv.slice(2))
    if (args.help) {
      process.stdout.write(`Usage: node scripts/swarm/generate-task-queue.mjs [options]

Options:
  --top N               max proposals (default 5)
  --dry-run             print to stdout only
  --max-age-days N      ignore violations older than N days (default 14)
  --violations PATH     override input path
  --analytics PATH      override input path
  --sentry PATH         override input path
  --manifest PATH       override manifest path
  --out PATH            output markdown path
  --now ISO             fake current time (testing)
`)
      process.exit(0)
    }
    await run(args)
  } catch (e) {
    process.stderr.write(`!! generate-task-queue failed: ${e.message}\n`)
    process.exit(1)
  }
}
