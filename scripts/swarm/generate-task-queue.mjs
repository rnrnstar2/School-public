const DEFAULT_MANIFEST = resolve(ROOT, 'docs/swarmops/journey-manifest.yaml')
const DEFAULT_OUT = resolve(ROOT, 'docs/swarmops/task-proposals.md')

const AUTO_BEGIN = '<!-- auto-generated:start -->'
const AUTO_END = '<!-- auto-generated:end -->'

const DEFAULT_HEADER = `# Task Proposals

> Auto-generated by \`scripts/swarm/generate-task-queue.mjs\`.
> この下の \`auto-generated\` ブロックだけが都度書き換わります。
> owner は候補を \`TASK_QUEUE.md\` に手動で起票してください（自動 merge しません）。
`

