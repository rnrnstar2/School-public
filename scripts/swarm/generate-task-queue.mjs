export function parseArgs(argv) {
  const args = {
    top: 5,
    dryRun: false,
    maxAgeDays: 14,
    violations: DEFAULT_VIOLATIONS,
    analytics: DEFAULT_ANALYTICS,
    sentry: DEFAULT_SENTRY,
    manifest: DEFAULT_MANIFEST,
    out: DEFAULT_OUT,
    now: null, // test hook (ISO string)
  }
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i]
    switch (a) {
      case '--dry-run':
        args.dryRun = true
        break
      case '--top':
        args.top = Number(argv[++i])
        break
      case '--max-age-days':
        args.maxAgeDays = Number(argv[++i])
        break
      case '--violations':
        args.violations = argv[++i]
        break
      case '--analytics':
        args.analytics = argv[++i]
        break
      case '--sentry':
        args.sentry = argv[++i]
        break
      case '--manifest':
        args.manifest = argv[++i]
        break
      case '--out':
        args.out = argv[++i]
        break
      case '--now':
        args.now = argv[++i]
        break
      case '--help':
      case '-h':
        args.help = true
        break
      default:
        throw new Error(`unknown flag: ${a}`)
    }
  }
  if (!Number.isFinite(args.top) || args.top <= 0) {
    throw new Error(`--top must be a positive number, got ${args.top}`)
  }
  if (!Number.isFinite(args.maxAgeDays) || args.maxAgeDays <= 0) {
    throw new Error(`--max-age-days must be a positive number, got ${args.maxAgeDays}`)
  }
  return args
}

// ---------------------------------------------------------------------------
// Manifest parser (shares convention with collect-criteria-violations.mjs)
// ---------------------------------------------------------------------------

export function parseManifestNodes(text) {
  const lines = text.split('\n')
  const nodes = []
  let i = 0
  while (i < lines.length) {
    const raw = lines[i]
    const line = raw.replace(/#.*$/, '').trimEnd()
    if (!line.trim()) {
      i++
      continue
    }
    const m = line.match(/^([a-z_]+):\s*(.*)$/)
    if (m && m[1] === 'nodes' && m[2] === '') {
      i++
      let current = null
      while (i < lines.length) {
        const l = lines[i]
        if (/^\s*-\s+id:/.test(l)) {
          if (current) nodes.push(current)
          current = { id: l.split(':')[1].trim() }
          i++
          continue
        }
        const kv = l.match(/^\s+([a-z_]+):\s*(.*)$/)
        if (kv && current) {
          let v = kv[2].trim()
          if (v === 'null') v = null
          else if (v === 'true') v = true
          else if (v === 'false') v = false
          else if (/^".*"$/.test(v)) v = v.slice(1, -1)
          // arrays like [a, b] are not needed for our purposes; keep raw
          current[kv[1]] = v
          i++
          continue
        }
        if (l.trim() === '' || /^#/.test(l.trim())) {
          i++
          continue
        }
        break
      }
      if (current) nodes.push(current)
      continue
    }
    i++
  }
  return nodes
}

